package controlstatment;

public class Switch {

	public static void main(String[] args) {
		
		int number =70;
		switch(number) {
		case 10:System.out.println("Number is 30");
		break;
		case 20:System.out.println("Number is 30");
		break;
		case 30:System.out.println("Number is 30");
		break;
		case 40:System.out.println("Number is 30");
		break;
		case 50:System.out.println("Number is 30");
		break;
		case 60:System.out.println("Number is 30");
		break;
		default:System.out.println("Number is not present");
		}

	}

}
