package operators;

public class Logical_Bitwise {

	public static void main(String[] args) {
		
		
		
		int a = 5;
		int b = 10;
		int c = 5;
		
		System.out.println(a>b && b>c);
		System.out.println(a>b || b>c);
		System.out.println("===================================");
		System.out.println(a>b & b>c);
		System.out.println(a>b | b>c);
		
	}

}
